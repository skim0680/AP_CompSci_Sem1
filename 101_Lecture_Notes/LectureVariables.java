/* 
    Lecture note example - Variables (String, int, double)
*/

class LectureVariables{
    public static void main(String args[]) {
        
        String name;
        name = new String("Robin");
        
        int number;
        number = 3;
        
        double money;
        money = 15.50;
        
        System.out.println(name + " lives in Vancouver");
        System.out.println(name + " has " + number + " Home Depots");
        System.out.println(name + " has " + money + " dollars worth of watermelons");
        
	}
}